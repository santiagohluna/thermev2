program TsolTliq
!   ---------------------------------------------------------------------------
    use thermev2_subs
!   ---------------------------------------------------------------------------
    implicit none
!   ---------------------------------------------------------------------------
    real(kind=dp) :: r
    real(kind=dp), parameter :: dr = 1.d2
!   ---------------------------------------------------------------------------
    open(unit=50,file='../out/TsolTliq.out')
!   ---------------------------------------------------------------------------
    r = Rc
!   ---------------------------------------------------------------------------
    do while(r.le.Rt)
        write(50,*) r,tsol(r),tliq(r)
        r = r + dr
    end do
!   ---------------------------------------------------------------------------
end program TsolTliq
