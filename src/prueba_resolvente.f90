!==============================================================================
    program prueba_resolvente
!   ---------------------------------------------------------------------------
        use thermev2_subs, only: fPio,rtbis,x0,Rc,Pcmb,Pec,rhoc,glm
!       -----------------------------------------------------------------------
        implicit none
    !   -----------------------------------------------------------------------
        real (kind=8) :: tcmb,ric,Pio,chi
        logical lPio
!      ------------------------------------------------------------------------
10      format(a8,1x,f8.2)
!       -----------------------------------------------------------------------
        print *,'Introducir el valor de Tcmb'
        read *,tcmb
!       -----------------------------------------------------------------------
        Ric = 0.d0
        chi = x0*Rc**3/(Rc**3-Ric**3)
        lPio = fPio(Pcmb,Tcmb,chi)*fPio(Pec,Tcmb,chi).lt.0.d0
        if(lPio) then 
            Pio = rtbis(fPio,Tcmb,chi,Pcmb,Pec,1.d-6)
            ric = dsqrt(2.d0*(pec-Pio)*1.d9*rc/(rhoc*glm))
        else
            Pio = Pec
            Ric = 0.d0
        end if
!       -----------------------------------------------------------------------
        print *,' '
        print 10,'Tcmb =',tcmb
        print 10,' Pio =',Pio
        print 10,' Ric =',ric/1000.d0
        print 10,'Ric/Rc =',Ric/Rc
        print *,' '
    !   -----------------------------------------------------------------------
    end program prueba_resolvente
!==============================================================================